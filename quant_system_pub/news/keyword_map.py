def score_sentiment(text: str) -> float:
    """
    快速情感打分：正数=利好，负数=利空，0=中性
    分值范围：-1.0 ~ +1.0
    """
    if not text:
        return 0.0
    pos = sum(1 for w in POSITIVE_WORDS if w in text)
    neg = sum(1 for w in NEGATIVE_WORDS if w in text)
    total = pos + neg
    if total == 0:
        return 0.0
    return round((pos - neg) / total, 2)


def score_urgency(text: str) -> int:
    """
    紧急程度打分：0~3
    出现高权重词（国务院/央行等）给3分，普通紧急词给1分
    """
    high_authority = ["国务院", "中央", "习近平", "李强", "政治局", "央行", "证监会", "发改委"]
    if any(w in text for w in high_authority):
        return 3
    if any(w in text for w in URGENT_WORDS):
        return 2
    return 1


def match_sectors(text: str) -> list[str]:
    """
    从文本中匹配受影响板块，返回去重列表
    """
    matched = []
    for keyword, sectors in KEYWORD_SECTOR_MAP.items():
        if keyword in text:
            matched.extend(sectors)
    # 去重同时保持顺序（优先级：先匹配的靠前）
    seen = set()
    result = []
    for s in matched:
        if s not in seen:
            seen.add(s)
            result.append(s)
    return result
